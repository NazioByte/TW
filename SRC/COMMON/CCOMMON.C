unsigned char stdtoku( unsigned char p_stdcode ) {
	return ( ( p_stdcode >= 0x80 ) ? std_to_ku[p_stdcode - 0x80] : p_stdcode );
}

unsigned char kutostd( unsigned char p_kucode ) {
	return ( ( p_kucode >= 0x80 ) ? ku_to_std[p_kucode - 0x80] : p_kucode );
}
